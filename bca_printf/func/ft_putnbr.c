#include "../ft_printf.h"

void	ft_putnbr(int n)
{
	char	c;

	c = '_';
	if (n == -2147483648)
		write(1, "2147483648", 10);
	else if (n < 0)
		ft_putnbr(-n);
	else
	{
		if (n > 9)
			ft_putnbr(n / 10);
		c = n % 10 + '0';
		write(1, &c, 1);
	}
}
